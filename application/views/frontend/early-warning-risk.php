            <h3 class="main_title_left">
				<em></em>Early Warning System for the Barangays
            </h3>

            <p>
            Through the CoB DRRM Project of Balanga City, the community were given the chance to get involved and take part in designing their own Early Warning System based on their Community Risk Assessment activities. The members of the community from different sectors were given the opportunity to share their ideas on keeping their families safe from the threat of hazards present in their barangay. Once the EWS of each barangay were validated by the representative of the community from different sectors especially residents living in the high risk areas, it was then finalized and printed on tarpaulins posted on different strategic areas. Further information dissemination on how the EWS of the barangay works were done through general assembly and per purok/kalye gathering system. The effectiveness and efficiency of EWS of each barangay were tested through evacuation drill/simulation before it can be adopted by the council. 
            </p>

